
namespace VideoLooper {
    /// <summary>
    /// how the media player behaves when the current media ends
    /// </summary>
    public enum PlaybackMode {

        /// <summary>
        /// stop playing
        /// </summary>
        Off,

        /// <summary>
        /// play the next item in the playlist
        /// </summary>
        On,

        /// <summary>
        /// re-play the current item
        /// </summary>
        Repeat
    }
}
